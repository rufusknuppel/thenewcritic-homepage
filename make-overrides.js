// Regenerates content-overrides.js — the per-post card overrides the build
// reads — from two sources:
//
//   1. The "Website Meta" spreadsheet, exported to a CSV in the repo root.
//      Supplies `kicker` and `author` in bulk, one row per post slug.
//   2. content-overrides-rich.js — hand-authored long/structured fields
//      (previews, focal points, title/dek/date corrections). These WIN
//      over the sheet on any field they set.
//
// Usage:
//   npm run overrides                 # auto-pick the newest matching CSV
//   node make-overrides.js <file.csv> # use a specific CSV
//
// The CSV stays uncommitted (it's your working export); content-overrides.js,
// content-overrides-rich.js, and this script are committed, so the build is
// reproducible from what's in the repo.

const fs = require('fs');
const path = require('path');

const ROOT = __dirname;
const OUT = path.join(ROOT, 'content-overrides.js');
const RICH = path.join(ROOT, 'content-overrides-rich.js');

// Minimal RFC4180 parser: handles quoted fields, commas/newlines inside
// quotes, escaped "" quotes, and CRLF.
function parseCSV(text) {
  const rows = [];
  let row = [], field = '', inQ = false;
  for (let i = 0; i < text.length; i++) {
    const c = text[i];
    if (inQ) {
      if (c === '"') {
        if (text[i + 1] === '"') { field += '"'; i++; }
        else inQ = false;
      } else field += c;
    } else if (c === '"') inQ = true;
    else if (c === ',') { row.push(field); field = ''; }
    else if (c === '\r') { /* ignore */ }
    else if (c === '\n') { row.push(field); rows.push(row); row = []; field = ''; }
    else field += c;
  }
  if (field.length || row.length) { row.push(field); rows.push(row); }
  return rows;
}

// Resolve which CSV to read: an explicit CLI arg, else the most recently
// modified repo-root CSV whose name mentions "content-overrides".
function resolveCsv() {
  const arg = process.argv[2];
  if (arg) {
    if (!fs.existsSync(arg)) throw new Error(`CSV not found: ${arg}`);
    return arg;
  }
  const candidates = fs.readdirSync(ROOT)
    .filter((f) => /\.csv$/i.test(f) && /content-overrides/i.test(f))
    .map((f) => ({ f, m: fs.statSync(path.join(ROOT, f)).mtimeMs }))
    .sort((a, b) => b.m - a.m);
  if (!candidates.length) {
    throw new Error(
      'No *content-overrides*.csv found in the repo root. Export the sheet ' +
      'there, or pass a path: node make-overrides.js <file.csv>'
    );
  }
  if (candidates.length > 1) {
    console.log('Multiple CSVs match; using the newest:');
    candidates.forEach((c, i) => console.log(`  ${i === 0 ? '→' : ' '} ${c.f}`));
  }
  return path.join(ROOT, candidates[0].f);
}

// Single-quoted JS string literal, matching the file's hand-written style.
const q = (s) => "'" + String(s).replace(/\\/g, '\\\\').replace(/'/g, "\\'") + "'";
function emitVal(v, indent) {
  if (Array.isArray(v)) {
    const pad = indent + '  ';
    return '[\n' + v.map((x) => pad + q(x) + ',').join('\n') + '\n' + indent + ']';
  }
  return q(v);
}

const HEADER = `// GENERATED by make-overrides.js — do NOT edit by hand.
// Edit the Website Meta CSV (kicker/author) or content-overrides-rich.js
// (previews, focal points, corrections), then run \`npm run overrides\`.
//
// Per-post card overrides, keyed by the post's URL slug — the part after
// /p/ in its address (…/p/luddite-club -> 'luddite-club'). An override
// follows its post everywhere it appears (hero, row cards, list pages,
// archive). Fields:
//   kicker  — courier header above the title
//   title   — the card headline
//   dek     — subheading under the title (Substack's subtitle)
//   author  — byline in the meta line, shown uppercase
//   date    — meta-line date text, e.g. 'Jun 30' (the like count stays automatic)
//   preview — paragraph preview (string, or array for the hero's multiple paragraphs)
//   focal   — CSS object-position for the cover crop, e.g. 'center 20%'

`;

const FIELD_ORDER = ['kicker', 'title', 'dek', 'author', 'date', 'preview', 'focal'];

function main() {
  const csvPath = resolveCsv();
  const rows = parseCSV(fs.readFileSync(csvPath, 'utf8'));
  if (!rows.length) throw new Error(`${path.basename(csvPath)} is empty`);

  // Map columns by header name so column order / extra columns don't matter.
  const head = rows[0].map((h) => h.trim().toLowerCase());
  const col = (name) => head.indexOf(name);
  const iSlug = col('slug');
  if (iSlug === -1) throw new Error(`${path.basename(csvPath)} has no "slug" column`);
  const iKicker = col('kicker');
  const iAuthor = col('author');

  const out = {};
  const order = [];
  for (const r of rows.slice(1)) {
    const slug = (r[iSlug] || '').trim();
    if (!slug) continue;
    const e = {};
    const k = iKicker > -1 ? (r[iKicker] || '').trim() : '';
    const a = iAuthor > -1 ? (r[iAuthor] || '').trim() : '';
    if (k) e.kicker = k;
    if (a) e.author = a;
    if (!Object.keys(e).length) continue;
    if (!out[slug]) order.push(slug);
    out[slug] = e;
  }

  // Overlay the hand-authored rich fields (they win on any conflict).
  let rich = {};
  if (fs.existsSync(RICH)) {
    delete require.cache[require.resolve(RICH)];
    rich = require(RICH);
  }
  for (const slug of Object.keys(rich)) {
    if (!out[slug]) { out[slug] = {}; order.push(slug); }
    Object.assign(out[slug], rich[slug]);
  }

  let body = '';
  for (const slug of order) {
    const e = out[slug];
    body += '  ' + q(slug) + ': {\n';
    for (const f of FIELD_ORDER) {
      if (e[f] === undefined || e[f] === '') continue;
      body += '    ' + f + ': ' + emitVal(e[f], '    ') + ',\n';
    }
    body += '  },\n';
  }

  fs.writeFileSync(OUT, HEADER + 'module.exports = {\n' + body + '};\n');

  const nAuthor = order.filter((s) => out[s].author).length;
  const nKicker = order.filter((s) => out[s].kicker).length;
  const nRich = Object.keys(rich).length;
  console.log(`Read  ${path.basename(csvPath)}`);
  console.log(`Wrote content-overrides.js — ${order.length} slugs ` +
    `(${nKicker} kickers, ${nAuthor} authors, ${nRich} rich)`);
}

main();
